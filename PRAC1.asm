.CODE
MAIN PROC

MOV AX,@DATA
MOV DS,AX

MOV AH,01H
INT 21H
SUB AL,48D
MOV BL,AL
MOV CX,BX

OUTER: 

CMP CX,0  

JL TERMINATE
PUSH CX

MOV CX,BX

INNER: 

CMP CX,0
JL GOTO

MOV AH,02H
MOV DX,02AH
INT 21H
MOV DX,' '
INT 21H

LOOP INNER
GOTO:
MOV AH,02H
MOV DX,0AH
INT 21H
MOV DX,0DH
INT 21H
POP CX
LOOP OUTER

TERMINATE:
MAIN ENDP
END MAIN