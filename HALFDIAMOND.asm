.MODEL SMALL
.STACK 100H
.DATA 

 N1 DB 0 
  N2 DB 0
   N3 DB 0
 I DB 0
 J DB 0
 K DB 0
 HOLDI DB 0
 HOLDN DB 0  
 LEVEL DB 0 
 ROWS DB 0
 
.CODE

MAIN PROC
    
    
MOV AH,01H
INT 21H
SUB AL,'0'
MOV N1,AL
MOV N2,AL
MOV N3,AL




MOV I,2
MUL I
SUB AL,1


MOV AH,AL
MOV ROWS,AH

MOV CL,1 


MOV AH,02H
MOV DL,0AH
INT 21H
MOV DL,0DH
INT 21H

LUPI:
CMP CL,ROWS
JG TERMINATE     
    
CMP CL,N2

JG P1
MOV LEVEL,CL
JMP PROCEED

P1:
MOV CH,ROWS
SUB CH,CL
ADD CH,1
MOV LEVEL,CH


PROCEED:

MOV BL,0
MOV K,'A'
LUPJ:

CMP BL,LEVEL
JGE CONTINUE

MOV AH,02H
MOV DL,K
INC K
INT 21H
MOV DL,' '
INT 21H

INC BL
JMP LUPJ

CONTINUE:

MOV AH,02H
MOV DL,0AH
INT 21H
MOV DL,0DH
INT 21H

   
    

         INC CL
         JMP LUPI
TERMINATE:    
    
    
    MAIN ENDP
END MAIN