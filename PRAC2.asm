.MODEL SMALL
.STACK 100H 

.DATA

 N DW 'A'

.CODE
MAIN PROC

MOV AX,@DATA
MOV DS,AX

MOV AH,01H
INT 21H
SUB AL,48D 

MOV BL,AL
MOV CX,BX
MOV N,CX
SUB N,CX
INC N 
MOV AH,02H
MOV DX,0AH
INT 21H
MOV DX,0DH
INT 21H  

OUTER: 

CMP CX,0  

JL TERMINATE
PUSH CX

MOV CX,N
 
MOV BX,'A' 
 
INNER: 

CMP CX,0
JL GOTO 



MOV AH,02H
MOV DX,BX
INC BX
INT 21H
MOV DX,' '
INT 21H

LOOP INNER
GOTO:
INC N  

MOV AH,02H
MOV DX,0AH
INT 21H
MOV DX,0DH
INT 21H
POP CX
LOOP OUTER

TERMINATE:
MAIN ENDP
END MAIN